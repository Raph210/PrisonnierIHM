    private class Tableau extends AbstractTableModel {
        private String[] entetes = {"NÂ°","Choix A","Choix B","Gain A","Gain B"};
        
        public Tableau() {
        }

        @Override
        public int getRowCount() {          
            return partie.getNbCoups();
        }
        @Override
        public String getColumnName(int columnIndex) {
        return entetes[columnIndex];
        }

        @Override
        public int getColumnCount() {
            return entetes.length;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            switch(columnIndex){
                case 0: return rowIndex +1;
                case 1: return partie.AaCoopere(rowIndex)?"C":"D";
                case 2: return partie.BaCoopere(rowIndex)?"C":"D"; 
                case 3: return partie.getGainA(rowIndex);
                case 4: return partie.getGainB(rowIndex);
                default: return "Erreur";
            }
        }
    }

